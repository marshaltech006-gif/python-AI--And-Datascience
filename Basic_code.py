# Introduction
print(3+2)   # addition(+)
print(3-2)   # subtraction(-)
print(3*2)   # multiplication(*)
print(3/2)   # division(/)
print(3**2) # exponential(**)
print(3%2)   # modulus(%)
print(3//2) # Floor division operator(//)
# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(2+5j))              # Complex
print(type('RupeshKumar'))   # String
print(type([1,2,3,4]))           # List 
print(type({'Name':'Rupesh'})) # Dictionary
print(type({9,5,6,7,4,}))    # Set
print(type((3,)))   # Tuple
print(type(4==4)) # Boolean 
print(type(4>=4)) # Boolean
     